    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try {
            String ref = txtRef.getText();
            String marque = txtMarque.getText();
            double prix = Double.parseDouble(txtPrix.getText());
            Machine machine = machineDao.findById(id);
            if (machine != null) {

                machine.setRef(ref);
                machine.setMarque(marque);
                machine.setPrix(prix);

                if (machineDao.update(machine)) {
                    JOptionPane.showMessageDialog(this, "Machine modifiée avec succès");
                    load();
                } else {
                    JOptionPane.showMessageDialog(this, "Échec de la modification de la machine");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Machine introuvable pour l'id spécifié");
            }
        } catch (RemoteException ex) {
            Logger.getLogger(MachineForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:

        try {
            Machine machine = machineDao.findById(id);

            if (machine != null) {
                if (machineDao.delete(machine)) {
                    JOptionPane.showMessageDialog(this, "Machine supprimée avec succès");
                    load();
                } else {
                    JOptionPane.showMessageDialog(this, "Échec de la suppression de la machine");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Machine introuvable pour l'id spécifié");
            }
        } catch (RemoteException ex) {
            Logger.getLogger(MachineForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

